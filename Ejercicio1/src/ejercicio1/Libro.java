
package ejercicio1;

public class Libro {
    
    public String isbn;
    public String titulo;
    public String autor;
    public int cantidadPaginas;

    public Libro() {
    }
    
    public Libro(String isbn, String titulo, String autor, int cantidadPaginas) {
        this.isbn = isbn;
        this.titulo = titulo;
        this.autor = autor;
        this.cantidadPaginas = cantidadPaginas;
    }
    
    
    
}
